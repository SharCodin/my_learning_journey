/*
- string for words, phrases, or any alphanumeric data for presentation, not calculation
- char for a single alphanumeric character
- int for a whole number
- decimal for a number with a fractional component
- bool for a true/false value
*/

Console.WriteLine('b'); // Character literal - char

Console.WriteLine(123); // Integer literal - int

/*
Float Type    Precision
----------------------------
float         ~6-9 digits
double        ~15-17 digits
decimal        28-29 digits
*/

Console.WriteLine(0.25F); // Floating point literal - float. F or f is called literal suffix

Console.WriteLine(2.625); // double - default without a literal suffix

Console.WriteLine(12.3981M); // decimal - most precision

Console.WriteLine(true); // Boolean literal - true bool
Console.WriteLine(false); // Boolean literal - false bool

Console.WriteLine("123"); // string
Console.WriteLine(123); // int

Console.WriteLine("true"); // string
Console.WriteLine(true); // bool
